/**
 * generate-tailwind-config.mjs
 *
 * Reads tokens.json and writes tailwind.config.js, mapping every token
 * to the CSS custom property that Style Dictionary already outputs.
 *
 * Usage: node scripts/generate-tailwind-config.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import { fileURLToPath } from 'url';

const ROOT   = join(fileURLToPath(import.meta.url), '../../');
const tokens = JSON.parse(readFileSync(join(ROOT, 'tokens.json'), 'utf8'));

const v = (name) => `var(--${name})`;

// ─── Colors ──────────────────────────────────────────────────────────────────

const colors = {};

// Primitive palettes  e.g. color.z[0], color.cta[600]
for (const [group, shades] of Object.entries(tokens.color)) {
  const key = group.toLowerCase();
  colors[key] = {};
  for (const shade of Object.keys(shades)) {
    colors[key][shade] = v(`color-${key}-${shade}`);
  }
}

// Tokens excluded from Tailwind colors — defined as @layer utilities in index.css
// to bypass Tailwind's opacity composition (rgb(var()) wrapping breaks rgba values).
const TAILWIND_EXCLUDED = new Set([
  'border-glass-default',
  'border-glass-medium',
  'border-glass-subtle',
  'border-inverted-subtle',
]);

// Semantic tokens — CSS vars handle light/dark automatically.
// Nested groups like bg/glass/* become flat: 'bg-glass-default', etc.
function walkSemantic(obj, prefix = '') {
  for (const [key, val] of Object.entries(obj)) {
    // Sanitise key for Tailwind (spaces, slashes → hyphens)
    const segment = key.toLowerCase().replace(/[\s/]+/g, '-');
    const path = prefix ? `${prefix}-${segment}` : segment;

    if (val && typeof val === 'object' && '$value' in val) {
      if (!TAILWIND_EXCLUDED.has(path)) {
        colors[path] = v(path);       // CSS var name matches SD output
      }
    } else if (val && typeof val === 'object') {
      walkSemantic(val, path);
    }
  }
}
walkSemantic(tokens.semantic.light);  // light is the canonical value; dark overrides via .dark {}

// ─── Spacing ─────────────────────────────────────────────────────────────────

const spacing = {};
for (const name of Object.keys(tokens.spacing)) {
  // e.g. 'p-4' → var(--spacing-p-4)
  spacing[name] = v(`spacing-${name}`);
}

// ─── Border radius ────────────────────────────────────────────────────────────

const borderRadius = {};
for (const name of Object.keys(tokens.radius)) {
  // e.g. 'radius-4' → var(--radius-radius-4)
  borderRadius[name] = v(`radius-${name}`);
}

// ─── Font sizes ───────────────────────────────────────────────────────────────
// Desktop values as baseline; responsive overrides come from typography.css.

const fontSize = {};

function walkTypography(obj, prefix = '') {
  for (const [key, val] of Object.entries(obj)) {
    const segment = key.toLowerCase().replace(/[\s/]+/g, '-');
    const path = prefix ? `${prefix}-${segment}` : segment;

    if (val && typeof val === 'object' && '$value' in val) {
      fontSize[path] = v(path);       // e.g. var(--font-size-headings-h1)
    } else if (val && typeof val === 'object') {
      walkTypography(val, path);
    }
  }
}
walkTypography(tokens.typography.desktop);

// Short aliases — canonical class names used in the codebase
const shortAliases = {
  'display-1':  'font-size-headings-display-h1',
  'display-2':  'font-size-headings-display-h2',
  'h1':         'font-size-headings-h1',
  'h2':         'font-size-headings-h2',
  'h3':         'font-size-headings-h3',
  'h4':         'font-size-headings-h4',
  'h5':         'font-size-headings-h5',
  'h6':         'font-size-headings-h6',
  'subheading': 'font-size-labels-subheading',
  'label-s':    'font-size-labels-label-s',
  'legal':      'font-size-labels-legal',
  'fine-print': 'font-size-labels-fine-print',
  'overline-m': 'font-size-labels-overline-m',
  'overline-s': 'font-size-labels-overline-s',
  'copy-m':     'font-size-copy-copy-m',
  'copy-s':     'font-size-copy-copy-s',
  'copy-xs':    'font-size-copy-copy-xs',
  'btn-m':      'font-size-ui-button-m',
  'btn-s':      'font-size-ui-button-s',
  'card-m':     'font-size-ui-card-m',
  'tag-m':      'font-size-ui-tag-m',
  'tag-s':      'font-size-ui-tag-s',
  'caption':    'font-size-ui-caption',
  'nav-l':      'font-size-ui-nav-l',
  'nav-m':      'font-size-ui-nav-m',
  'nav-s':      'font-size-ui-nav-s',
  'tooltip':    'font-size-ui-tooltip',
  'chip-s':     'font-size-ui-chip-s',
  'chip-xs':    'font-size-ui-chip-xs',
  'tooltip-kbd':'font-size-ui-tooltip-kbd',
  'brand-logo': 'font-size-brand-logo',
};
for (const [alias, canonical] of Object.entries(shortAliases)) {
  fontSize[alias] = v(canonical);
}

// ─── Write tailwind.config.js ─────────────────────────────────────────────────

const js = `// Do not edit directly — generated by scripts/generate-tailwind-config.mjs
// Tokens are sourced from tokens.json via Style Dictionary CSS custom properties.
// Run: npm run tokens

/** @type {import('tailwindcss').Config} */
export default {
  theme: {
    extend: {
      colors: ${JSON.stringify(colors, null, 6).replace(/"var\(([^)]+)\)"/g, "'var($1)'")},

      spacing: ${JSON.stringify(spacing, null, 6).replace(/"var\(([^)]+)\)"/g, "'var($1)'")},

      borderRadius: ${JSON.stringify(borderRadius, null, 6).replace(/"var\(([^)]+)\)"/g, "'var($1)'")},

      fontSize: ${JSON.stringify(fontSize, null, 6).replace(/"var\(([^)]+)\)"/g, "'var($1)'")},
    },
  },
};
`;

writeFileSync(join(ROOT, 'tailwind.config.js'), js);

const counts = {
  colors: Object.keys(colors).length,
  spacing: Object.keys(spacing).length,
  borderRadius: Object.keys(borderRadius).length,
  fontSize: Object.keys(fontSize).length,
};
console.log('tailwind.config.js written');
console.log(`  colors: ${counts.colors}  spacing: ${counts.spacing}  borderRadius: ${counts.borderRadius}  fontSize: ${counts.fontSize}`);
